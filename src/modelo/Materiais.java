package modelo;

public class Materiais {
	private int codMaterial;
	private int codIdioma;
	private int codTipoMaterial;
	private String titulo;
	private String subtitulo;
	private String descricao;
	
	public int getCodMaterial() {
		return codMaterial;
	}
	public void setCodMaterial(int codMaterial) {
		this.codMaterial = codMaterial;
	}
	public int getCodIdioma() {
		return codIdioma;
	}
	public void setCodIdioma(int codIdioma) {
		this.codIdioma = codIdioma;
	}
	public int getCodTipoMaterial() {
		return codTipoMaterial;
	}
	public void setCodTipoMaterial(int codTipoMaterial) {
		this.codTipoMaterial = codTipoMaterial;
	}
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	public String getSubtitulo() {
		return subtitulo;
	}
	public void setSubtitulo(String subtitulo) {
		this.subtitulo = subtitulo;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	
	
}
